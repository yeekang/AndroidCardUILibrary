package com.jhy.androidcarduilibrary;

public class Opportunitymap {

    public static String title;
    public static String desc;
    public static String url;
    public static int seriesCount;

    public Opportunitymap(String title, String desc, String url, int seriesCount) {
        Opportunitymap.title = title;
        Opportunitymap.desc = desc;
        Opportunitymap.url = url;
        Opportunitymap.seriesCount = seriesCount;
    }
}